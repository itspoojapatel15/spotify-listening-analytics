CREATE WAREHOUSE IF NOT EXISTS SPOTIFY_WH WAREHOUSE_SIZE='X-SMALL' AUTO_SUSPEND=60 AUTO_RESUME=TRUE;
CREATE DATABASE IF NOT EXISTS SPOTIFY_DB;
USE DATABASE SPOTIFY_DB;
CREATE SCHEMA IF NOT EXISTS RAW_SPOTIFY;
CREATE SCHEMA IF NOT EXISTS STAGING;
CREATE SCHEMA IF NOT EXISTS MARTS;
CREATE SCHEMA IF NOT EXISTS SNAPSHOTS;
CREATE ROLE IF NOT EXISTS SPOTIFY_ROLE;
GRANT USAGE ON WAREHOUSE SPOTIFY_WH TO ROLE SPOTIFY_ROLE;
GRANT ALL ON DATABASE SPOTIFY_DB TO ROLE SPOTIFY_ROLE;
GRANT ALL ON ALL SCHEMAS IN DATABASE SPOTIFY_DB TO ROLE SPOTIFY_ROLE;
GRANT ALL ON FUTURE SCHEMAS IN DATABASE SPOTIFY_DB TO ROLE SPOTIFY_ROLE;
GRANT ALL ON FUTURE TABLES IN DATABASE SPOTIFY_DB TO ROLE SPOTIFY_ROLE;
